package exercicio;

public class Exercicio1305 {
	public static void main(String[] args) {
		int a = 10, b = 15;
		double c = -5.9, d = 4.5;
		System.out.println("O maior valor entre 10 e 15 é : "+Math.max(a, b));
		System.out.println("O maior valor entre -5.9 e 4.5 é : "+Math.max(c, d));
		
		System.out.println("-----------------------------------");
		
		double e = 25, f = 2;
		
		System.out.println("A raiz quadrada de "+e+" é = "+Math.sqrt(e));
		System.out.println("Potencia = "+ Math.pow(e, f));
	}
}
