package exemplo;

public class Variaveis {
	public static void main(String[] args) {
		/*Variaveis do tipo primitivo*/
		
		// Pra valores inteiros temos:
		int inteiro = 10;
		long inteiroMaior = 100000L;
		
		// Para valores reais temos:
		float real = 10.0f;
		double realMaior = 30.11;
		
		// Para valores letras temos:
		char letra = 'L';
				
		// para valores booleanos temos:
		boolean booleano = true; // ou false
		
		/* Variaveis do tipo Wrapper (class) */
		
		// Para valores inteiro temos:
		Integer wInteiro = 30;
		Long wInteiroMaior = 1000000l;
		
		// Para valores reais temos:
		Float wReal = 10.0f;
		Double wRealMaior = 31.0;
		
		// Para valores texto temos:
		String wTexto = "Segunda aula de Java";
		
		// Para valores do tipo booleano temos:
		Boolean wBooleano = false; // or true
	}
}
